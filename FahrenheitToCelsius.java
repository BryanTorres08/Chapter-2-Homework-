public class FahrenheitToCelsius {


    public static void main(String[] args) {


        double temeperatureFahrenheit = 100;


        double temperatureCelsius = (temeperatureFahrenheit - 32) * 5/9;


        System.out.printf("%.2f Fahrenheit is equal to %.2f Celsius%n", temeperatureFahrenheit, temperatureCelsius);


    }
}
